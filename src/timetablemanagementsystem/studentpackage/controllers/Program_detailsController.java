    @FXML
    private void handleButtonAction(ActionEvent event) throws SQLException {
    if(event.getSource() == insert2){
        InsertRecord();
        CancelRecord();
    }else if(event.getSource() == update2){
        if(searchid == null){
            searchbar.setPromptText("PLEASE ENTER ID TO SEARCH");
        }else{
            UpdateRecord();
            CancelRecord();
        }
    }else if (event.getSource() == delete2){
        if(searchid == null){
            searchbar.setPromptText("PLEASE ENTER ID TO SEARCH");
        }else{
            deleteButton();
            CancelRecord();
        }
    }else if (event.getSource() == cancel2){
        CancelRecord();
    }else if (event.getSource() == search){
        if (!(searchbar.getText().isEmpty())){
            SearchRecord();
        }
        else{
            searchbar.setPromptText("PLEASE ENTER ID TO SEARCH");
        }
    }
        
    }
    
    public Connection getconnection(){
         Connection conn;
        try{
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/timetable","root","");
            return conn;
        }catch (Exception ex){
            System.out.println("Error: " + ex.getMessage());
            return null;
        }
}
    
      public ObservableList<programdetails> getprogramdetailslist(){
        ObservableList<programdetails> Programdetailslist = FXCollections.observableArrayList();
        Connection conn = getconnection();
        String query = "SELECT * FROM Programdetails";
        Statement st;
        ResultSet rs;
          
        try {
            
        st = conn.createStatement();
        rs = st.executeQuery(query);
        programdetails Programdetails;
        while(rs.next()){
            Programdetails = new programdetails(rs.getString("id"), rs.getString("p_name"), rs.getString("pf_name"));
            Programdetailslist.add(Programdetails);
            
        }
    }catch (Exception ex){
        ex.printStackTrace();
    }
        return Programdetailslist;
   }
    public void showprogramdetails(){
        ObservableList<programdetails> list = getprogramdetailslist();
        colid0.setCellValueFactory(new PropertyValueFactory<programdetails ,String>("id"));
        colid1.setCellValueFactory(new PropertyValueFactory<programdetails ,String>("p_name"));
        colid2.setCellValueFactory(new PropertyValueFactory<programdetails ,String>("pf_name"));
        
        
        table2.setItems(list);
        
    }
    private void InsertRecord(){
        String query = "INSERT INTO programdetails(p_name,pf_name) VALUES('"+tfd1.getText()+"','"+tfd2.getText()+"')";
        executeQuery(query);
        showprogramdetails();
        searchid = null;
    }
    private void UpdateRecord(){
        String query = "UPDATE Programdetails SET p_name = '" + tfd1.getText() + "' , pf_name = '" + tfd2.getText() +"' where id ="+searchid+"";
        executeQuery(query);
        showprogramdetails();
        searchid = null;
    }
    private void deleteButton(){
        String query = "DELETE FROM Programdetails where id ="+searchid+"";
        executeQuery(query);
        showprogramdetails();
        searchid = null;
    }
    private void CancelRecord(){
        
        tfd1.setText("");
        tfd2.setText("");
       
        searchbar.setText("");
        
        showprogramdetails();
    }
            
    private void executeQuery(String query) {
        Connection conn = getconnection();
        Statement st;
        try{
            st = conn.createStatement();
            st.executeUpdate(query);
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    }

    private void SearchRecord() throws SQLException {
        
        searchid = searchbar.getText();
        
        ObservableList<programdetails> Programdetailslist = FXCollections.observableArrayList();
        Connection conn = getconnection();
        String query = "SELECT * FROM Programdetails where id="+searchid+"";
        Statement st;
        ResultSet rs;
            
        try{
            st = conn.createStatement();
            rs = st.executeQuery(query);
           
        while(rs.next()){
            tfd1.setText(rs.getString("p_name"));
            tfd2.setText(rs.getString("pf_name"));
            
        }
        
        }catch (Exception ex){
        ex.printStackTrace();
    }
        
        
    }

